# إنشاء نسخة واحدة من البوت لتعمل على السيرفر
bot_instance = GeminiChatBot()

@app.get("/")
def home():
    return {"message": "Gemini 2.5 Flash API is Running"}

@app.post("/chat")
async def chat_endpoint(
    text: str = Form(None),
    image: UploadFile = File(None),
    audio: UploadFile = File(None)
):
    """
    نقطة الاتصال (API Endpoint)
    تستقبل النص والصورة والصوت، وتحفظ الملفات مؤقتاً لتمريرها لكودك
    """
    temp_image_path = None
    temp_audio_path = None

    try:
        # حفظ الصورة مؤقتاً إذا وجدت (لأن كودك يطلب مسار ملف)
        if image:
            temp_image_path = f"temp_{image.filename}"
            with open(temp_image_path, "wb") as buffer:
                shutil.copyfileobj(image.file, buffer)

        # حفظ الصوت مؤقتاً إذا وجد
        if audio:
            temp_audio_path = f"temp_{audio.filename}"
            with open(temp_audio_path, "wb") as buffer:
                shutil.copyfileobj(audio.file, buffer)

        # استدعاء دالتك الأصلية بنفس طريقتك
        response_text = bot_instance.send_message(
            text=text,
            image_path=temp_image_path,
            audio_path=temp_audio_path
        )

        return {"response": response_text}

    finally:
        # تنظيف الملفات المؤقتة بعد الرد لعدم ملء السيرفر
        if temp_image_path and os.path.exists(temp_image_path):
            os.remove(temp_image_path)
        if temp_audio_path and os.path.exists(temp_audio_path):
            os.remove(temp_audio_path)

