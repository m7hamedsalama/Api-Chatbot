from fastapi import FastAPI, UploadFile, File, Form
import google.generativeai as genai
import PIL.Image
import time
import os
import shutil

app = FastAPI()

# ==============================================================================
# كودك الأصلي (كما هو تماماً دون أي تعديل في المنطق)
# ==============================================================================

# 1. إعدادات المشروع
MY_API_KEY = os.environ.get("GOOGLE_API_KEY")

MODEL_NAME = "gemini-2.5-flash"

genai.configure(api_key=MY_API_KEY)

class GeminiChatBot:
    def __init__(self):
        # تهيئة الموديل
        self.model = genai.GenerativeModel(MODEL_NAME)
        # بدء جلسة محادثة فارغة (لحفظ التاريخ)
        self.chat_session = self.model.start_chat(history=[])
        print(f"🤖 تم تشغيل البوت باستخدام موديل: {MODEL_NAME}")

    def upload_audio(self, audio_path):
        """دالة مساعدة لرفع الملفات الصوتية وانتظار معالجتها"""
        if not os.path.exists(audio_path):
            return None

        print(f"🎙️ جاري رفع ومعالجة الملف الصوتي: {audio_path}...")
        audio_file = genai.upload_file(path=audio_path)

        # حلقة انتظار حتى تنتهي جوجل من معالجة الملف
        while audio_file.state.name == "PROCESSING":
            time.sleep(1)
            audio_file = genai.get_file(audio_file.name)

        if audio_file.state.name == "FAILED":
            print("❌ فشل في معالجة الملف الصوتي")
            return None

        print("✅ الملف الصوتي جاهز.")
        return audio_file

    def send_message(self, text=None, image_path=None, audio_path=None):
        """
        الدالة الرئيسية التي تستقبل أي نوع من المدخلات وترد عليها
        """
        input_parts = []

        # 1. تجهيز النص
        if text:
            input_parts.append(text)

        # 2. تجهيز الصورة (إن وجدت)
        if image_path:
            try:
                img = PIL.Image.open(image_path)
                input_parts.append(img)
                print("📷 تم إرفاق صورة.")
            except Exception as e:
                return f"❌ خطأ في الصورة: {e}"

        # 3. تجهيز الصوت (إن وجد)
        if audio_path:
            audio_file = self.upload_audio(audio_path)
            if audio_file:
                input_parts.append(audio_file)
            else:
                return "❌ تعذر رفع الملف الصوتي."

        # إذا لم يكن هناك مدخلات
        if not input_parts:
            return "⚠️ لا يوجد مدخلات للإرسال!"

        # 4. الإرسال للموديل والحصول على الرد
        try:
            # نستخدم chat_session لإرسال الرسالة وحفظ السياق
            response = self.chat_session.send_message(input_parts)
            return response.text
        except Exception as e:
            return f"❌ خطأ في النظام: {e}"

    def get_history(self):
        """لعرض تاريخ المحادثة بالكامل"""
        return self.chat_session.history

# ==============================================================================
# نهاية كودك الأصلي
